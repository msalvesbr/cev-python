# Desafios aula 7
titulo = 'Desafios aula 7 - média das notas:'
print('{:=^70}'.format(titulo))
n1 = float(input('Digite a primeira nota: '))
n2 = float(input('Digite a segunda nota: '))
media = (n1+n2)/2
print('*-'*35)
print('A média entre {:.1f} e {:.1f} é {:.1f}'.format(n1, n2, media))
print('*-'*35)
